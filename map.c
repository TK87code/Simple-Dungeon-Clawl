static int buffer[MAP_HEIGHT][MAP_WIDTH] = { - 1 };

void map_init(void)
{
	memcpy(buffer, map, sizeof(map));
}

void map_draw(int center_x, int center_y, int tile_size, int render_range_x, int render_range_y){
	memcpy(buffer, map, sizeof(map));
	
	// Add monster on the map to draw
	monster **monsters = monster_get_all();
	for (int i = 0; i < MAX_MONSTER_NUMBER; i++)
	{
		monster *Monster = monsters[i];
		if (Monster != NULL && Monster->is_dead == 0)
		{
			buffer[Monster->map_pos_y][Monster->map_pos_x] = MAP_ENEMY;
		}
	}
	
	int draw_starts_y = 0;
	int draw_starts_x = 0;
    
	for (int h = center_y - render_range_y; h < center_y + render_range_y + 1; h++)
	{
		for (int w = center_x - render_range_x; w < center_x + render_range_x + 1; w++)
		{
			// If the specified coordinates is beyond map size, skip
			if (w < 0 || w > MAP_WIDTH - 1 || h < 0 || h > MAP_HEIGHT - 1)
			{
				draw_starts_x++;
				continue;
			}
            
			switch (buffer[h][w]) {
				case MAP_EMPTY:
                DrawRectangle(draw_starts_x * tile_size, draw_starts_y * tile_size, tile_size, tile_size, LIGHTGRAY);
                break;
				case MAP_WALL:
                DrawRectangle(draw_starts_x * tile_size, draw_starts_y * tile_size, tile_size, tile_size, GRAY);
                break;
				case MAP_CLOSED_DOOR:
                DrawRectangle(draw_starts_x * tile_size, draw_starts_y * tile_size, tile_size, tile_size, DARKBROWN);
                break;
				case MAP_OPEND_DOOR:
                DrawRectangle(draw_starts_x * tile_size, draw_starts_y * tile_size, tile_size, tile_size, BROWN);
                break;
				case MAP_ENEMY:
                DrawRectangle(draw_starts_x * tile_size, draw_starts_y * tile_size, tile_size, tile_size, RED);
                break;
			}
			draw_starts_x++;
		}
		draw_starts_x = 0;
		draw_starts_y++;
	}
    
}

int map_check_collision(int map_id)
{
	if (map_id == MAP_WALL || map_id == MAP_CLOSED_DOOR || map_id == MAP_ENEMY)
	{
		return(1);
	}
	else
	{
		return(0);
	}
}

int map_get_cell(int x, int y)
{
	return buffer[y][x];
}

void map_open_door(int x, int y)
{
	map[y][x] = MAP_OPEND_DOOR;
}